              <div class="sc-Faq-question" data-behavior="sc-faq-trigger">
                What are Betterment's fees?
              </div>
              <div class="sc-Faq-answer" data-behavior="sc-faq-answer">
                <p>Betterment’s online financial advice is available for 0.25% per year with no minimum balance. We call this our Digital plan because you receive our advice online, through email, and through your mobile app. For 0.40% per year, you can also gain over-the-phone access to our team of CFP® professionals and licensed financial experts who can provide in-depth advice on investments outside of Betterment. This Premium plan requires a minimum balance of $100,000. <a href="pricing/index.html">Learn more about our pricing</a></p>
<p>For balances in excess of $2 million (excluding 401(k) balances through <a href="http://business.betterment.com/">Betterment for Business</a>), the Digital plan charges 0.15% per year and the Premium plan charges 0.30% on the portion of your balance above $2 million. Balances are calculated based on household. If you qualify for part of your balance managed free and have a balance above $2 million, the dollars managed free will be those with the higher fee.</p>
<p>For either plan, we calculate your fee daily so we can accurately account for deposits, withdrawals and market fluctuations that occur. We then total the fees from those daily calculations each quarter, billing you a fraction of the total annual fee. This means that if you withdraw your balance before the end of the quarter, you’re only charged the fee for the days your money was managed by Betterment.</p>
<p>What does the fee get you? It covers the advice you receive, the transactions, trades, transfers, and rebalancing we manage for you, and all other account administration. We do not charge you additional transaction fees to buy and sell securities. </p>
<p>Any fund held in your portfolio at Betterment will assess fund-level fees, called expense ratios, but Betterment aims to keep these costs low, and we receive no part of these fees.</p>
