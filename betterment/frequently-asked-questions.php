              <div class="sc-Faq-question" data-behavior="sc-faq-trigger">
                How do I move my money to Betterment?
              </div>
              <div class="sc-Faq-answer" data-behavior="sc-faq-answer">
                <p>There are two primary ways to move money to Betterment:</p>
<ol>
<li>By transferring investments from an account at a different firm, and </li>
<li>By linking a bank account to make cash deposits.</li>
</ol>
<p>If you roll over 401(k) or IRA assets to Betterment, your previous provider will typically sell the investments with no tax consequence and transfer the cash value to Betterment. If you wish to transfer investments from a personal brokerage account, you may be eligible for an <a href="https://help.betterment.com/hc/en-us/articles/115004260386">ACATS transfer</a>, which moves your holdings to Betterment without selling them first.
<p><a href="https://help.betterment.com/hc/en-us/articles/115004239183">Learn more about transferring existing investments to Betterment.</a></p>
<p>In addition to transfers, any customer can link their bank account to Betterment to make cash deposits, including regular automatic deposits. To help prevent fraud, Betterment customers can link only one bank account at a time for deposits. You can sync as many bank accounts as you wish to simply see all your assets in one place.</p>
