              <div class="sc-Faq-question" data-behavior="sc-faq-trigger">
                What is Betterment, exactly? How does it work?
              </div>
              <div class="sc-Faq-answer" data-behavior="sc-faq-answer">
                <p>Betterment is an online financial advisor. We help you plan your <a href="resources/research/goals-advice-explained/index.html">financial goals</a> and recommend <a href="resources/adjust-allocation-automatically/index.html">smart investment choices</a>, so that you save time and gain peace of mind, knowing that your money is working for you. We’re a fiduciary, which means we put your interests first.</p>
<p>When you open a Betterment account, we advise you on what your portfolio should include, while giving you control over <a href="resources/research/stock-allocation-advice/index.html">how much risk you take on</a>. All of <a href="portfolio/index.html">our portfolio strategies</a> are globally diversified and built using index-tracking exchange-traded funds (ETFs). We <a href="resources/research/etf-portfolio-selection-methodology/index.html">pick the funds</a> for you, making recommendations that control costs and taxes. We also automatically take care of things like portfolio rebalancing, reinvesting dividends, and <a href="tax-loss-harvesting/index.html">tax loss harvesting</a>.</p>
<p>When you make a deposit, we invest all of your money automatically, trading fractional shares for you in the background. That means there’s no extra step to turn your cash into investments, and your returns aren’t dragged down by holding extra cash.</p>

