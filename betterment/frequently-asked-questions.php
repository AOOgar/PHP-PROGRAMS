              <div class="sc-Faq-question" data-behavior="sc-faq-trigger">
                How does Betterment compare to other robo-advisors?
              </div>
              <div class="sc-Faq-answer" data-behavior="sc-faq-answer">
                <p>Like Betterment, most robo-advisors use technology to recommend a portfolio and automate the investment process. Betterment doesn't stop there. We’re designed to help you identify your financial goals, choose a portfolio for each of your goals, automate your savings, and keep your taxes and fees low. Then, when you have questions, licensed experts from our Support and Advice teams are ready over the phone, or to refer you to a one-on-one financial advisor.</p>
<p>In other words, Betterment aims to help you answer, “How can I be a smarter, better investor across my finances?” while other robo-advisors only answer “What should my portfolio look like?”</p>
<p>Betterment’s digital advice comes with no minimum balance and low, straightforward fees. Learn more about how we <a href="comparison/smarter-online-investing/index.html">compare to other robo-advisors</a>.</p>
